// There are 8 basic data types in JavaScript.

const number = 1; // number type

const string = '1'; // string type

const boolean = true; // also could be false

const nullable = null; // null type

const undef = undefined; // undefined type

const symbol = Symbol(); // symbol (unique value) type

const array = [1, '1', true, null, undefined]; // array (set of values) type

const object = {
  number,
  string,
  boolean,
  nullable,
  undef,
  symbol,
  array,
}; // complex data type
