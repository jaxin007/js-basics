// There are three types of conversion
// The first rule to know is there are only three types of conversion in JavaScript:
//
// to string
// to boolean
// to number

console.log(true + false); // 1 - to number
console.log(12 / '6'); // 2 - to number
console.log(12 + '16'); // 1216 - to string
console.log(!false); // true - to boolean
